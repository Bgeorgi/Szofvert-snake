    @Test
    public void decreseCellsTest() {
        gameState = new GameState();
        int[][] board = gameState.getGameState();

        int sum = 0;
        for (int i = 0; i < 20; i++) {
            for (int j = 0; j < 26; j++) {
                if (board[i][j] == -1) {
                    continue;
                } else {
                    sum+=board[i][j];
                }
            }
        }

        gameState.decreseCells();

        int sum1 = 0;
        for (int i = 0; i < 20; i++) {
            for (int j = 0; j < 26; j++) {
                if (board[i][j] == -1) {
                    continue;
                } else {
                    sum1+=board[i][j];
                }
            }
        }

        boolean isGreater=sum > sum1;
        assertTrue(isGreater);
    }


